package 常量的概述和使用.数学处理类.Math类;

public class MathTest {
    public static void main(String[] args) {

        System.out.println("获取连个整数最大值" + Math.max(10, 20));
        System.out.println("获取连个整数最小值" + Math.min(10, 20));
        System.out.println("获取次方的结果是" + Math.pow(2, 3));
        System.out.println("获取绝对值的是" + Math.abs(-5));
        System.out.println("进行四舍五入结果是" + Math.round(3.14));
        System.out.println("整数的平方根结果是" + Math.sqrt(16));
        System.out.println("生成的随机数是" + Math.random());


    }
}
